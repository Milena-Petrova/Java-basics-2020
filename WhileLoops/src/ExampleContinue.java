public class ExampleContinue {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 1) {//в случая пропускаме да принтване нечетните числа
                continue;//с този оператор не се изпълнява целият цикъл, както при брейк, цонтиниу прекъсва САМО текущата итерация и продължава цикъла отгоре
                //брейк прекъсва и ИЗЛИЗА от цикъла
            }
                System.out.println(i);

        }
    }
}
